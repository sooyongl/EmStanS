#' dataReady_setup
dataReady_setup <- function(imprt_data) {

  setup <- imprt_data[[1]]
  setup_name <-names(setup)# c("GCA", "Content Area", "Grade Group", "Number of Tables per group","Level Options", "SD", "EC")
  # names(setup) <- setup_name

  lv_p <- get_which(upper_remove_blank(setup_name), "LEVELOPTIONS")
  level_opt_name <- setup_name[lv_p]

  lv_vec <- map(str_split(setup[[lv_p]], ","), remove_blank_vector)

  lv_vec1 <- lv_vec %>%
    lapply(., function(x) {
      sapply(1:length(x), function(xx) paste0("Level",xx) )}
    ) %>%
    map(., ~paste(.x, collapse = ", ")) %>%
    do.call("rbind", .)

  setup[[level_opt_name]] <-  lv_vec1
  setup$GCA <- factor(setup$GCA, levels = setup$GCA)

  setup_data  <- setup %>% arrange(!!as.name(setup_name[1]))
  return(setup_data)
}
