#'
dataReady_examineedata <- function(imprt_data){

  examinee <- imprt_data[[5]]
  gca_id <- imprt_data[[1]][1]

  if(dim(examinee)[1] == 0) {
    examinee_data <- bind_cols(score = -999, freq = 1, gca_id)
  } else {
    examinee_data <- suppressMessages(reorganize_examinee(examinee))
    examdt_name <- names(examinee_data)

    gca_p <- get_which(upper_remove_blank(examdt_name), "GRADE|GCA")

    names(examinee_data)[gca_p] <- "GCA"
    examinee_data <- examinee_data %>% arrange(GCA)
  }

  if(length(examinee_data[[3]]) != length(gca_id)) {

    dummy_data <- bind_cols(score = -999, freq = 1, gca_id)
    dummy_data <- dummy_data %>% filter(!GCA %in% examinee_data[[3]])

    examinee_data <- bind_rows(examinee_data, dummy_data) %>%
      mutate(GCA = factor(GCA, levels = pull(gca_id, GCA))) %>%
      arrange(GCA) %>%
      mutate(GCA = as.character(GCA))

  }

  return(examinee_data)
}

get_which <- function(inp, char_match, add_position = 0) {
  which(str_detect(inp, char_match)) + add_position
}

remove_blank_vector <- function(inpData) {
  inpData %>% stri_replace_all_charclass(., "\\p{WHITE_SPACE}", "")
}
#'
upper_remove_blank <- function(vec){
  toupper(remove_blank_vector(vec))
}
#' remove_blank
remove_blank <- function(inp_data) {
  vec <- inp_data %>% stri_replace_all_charclass(., "\\p{WHITE_SPACE}", "")
  vec <- toupper(vec)
  return(vec)
}

#' reorganize_examinee
reorganize_examinee <- function(inp_data){
  if(sum(str_detect(toupper(names(inp_data)), toupper("freq"))) == 0){
    return(inp_data)
  } else {
    inpdata_reorg <- vector("list", ncol(inp_data))

    for(i in 1:ncol(inp_data)) {
      if(sum(is.na(inp_data[i])) == nrow(inp_data)){
        next
      }
      inpdata_reorg[[i]] <- inp_data[i]
    }
    inpdata_reorg <- inpdata_reorg %>% bind_cols()

    inpdata_reorg <- lapply(1:(ncol(inpdata_reorg)/3), function(i) {
      ii = 1 + (i - 1)*3
      iii = ii + 2
      inpdata_reorg[,ii:iii] %>% drop_na() %>%
        set_names(., nm = c("score", "freq","GCA"))
    })

    inpdata_reorg <- do.call('rbind',inpdata_reorg)

    return(inpdata_reorg)
  }
}
#' get_ID
get_ID <- function(filtered_data) {

  filter_name <- names(filtered_data)

  GCAID <- filtered_data %>% distinct(GCA) %>% arrange(GCA) %>% pull() %>% as.character()
  TableID <- filtered_data %>% distinct(GCA, Table) %>% arrange(GCA)

  Table_n <- TableID %>% group_by(GCA) %>% count() %>% pull(n)

  p1 <- as.name(filter_name[str_detect(filter_name, "Panel|User")])

  UserID <- filtered_data %>% distinct(GCA, Table, !!p1) %>% arrange(GCA)
  names(UserID) <- c("GCA", "Table", "User")

  out <- list(GCA = GCAID,
              Table = TableID,
              Table_n = Table_n,
              PanelID = UserID)

  return(out)
}
#' get_SD
get_SD <- function(setup_data, id_list){

  combined_id <- id_list$GCA
  GCA_id <- str_split(id_list$GCA, "-", simplify = T)[,1]

  SD_data =
    tryCatch({

      a1 <- data.frame(GCA = GCA_id) %>%
        left_join(., setup_data, by = "GCA") %>%
        mutate(GCA = combined_id) %>% pull(SD)

      a1[which(is.na(a1))] <- 1
      a1
    },
    error = function(e) 1
    )

  SD_data <- data.frame(GCAid = combined_id, SD = SD_data)
}

#' get_EC
get_EC <- function(setup_data, id_list){

  combined_id <- id_list$GCA
  GCA_id <- str_split(id_list$GCA, "-", simplify = T)[,1]

  EC_data =
    tryCatch({
      a1 <- data.frame(GCA = GCA_id) %>%
        left_join(., setup_data, by = "GCA") %>%
        mutate(GCA = combined_id) %>%
        select(contains("EC")) %>%
        pull()
      a1[which(is.na(a1))] <- 0
      a1
    },
    error = function(e) 0
    )

  EC_data <- data.frame(GCAid = id_list$GCA, EC = EC_data)
}

#' get_lvnm
get_lvnm <- function(inp_data, id_list){
  # inp_data = level_nm0; id_list

  combined_id <- id_list$GCA
  combined_df <- str_split(combined_id, "-", simplify = T)
  GCA_id <- unique(combined_df[,1])

  .get_lvnm <-
    function(inp_data) {
      lvnm <-
        inp_data %>%
        str_split(., ",", simplify = T) %>%
        stri_replace_all_charclass(.,"\\p{WHITE_SPACE}", "")

      return(lvnm)
    }

  temp1 <- apply(inp_data, 1, .get_lvnm) %>%
    data.frame() %>%
    set_names(., nm = GCA_id) %>%
    t() %>%
    data.frame() %>%
    set_names(paste0("y",1:ncol(.))) %>%
    data.frame(., X1 = rownames(.))

  out_df <- data.frame(combined_df) %>%
    set_names(nm = paste0("X", 1:ncol(combined_df))) %>%
    left_join(., temp1, by = "X1")


  text1 <- glue("paste({paste0('X',1:ncol(combined_df), collapse = ',' )}, sep = '-')")
  out_df <-
    out_df %>%
    mutate(GCA_temp = eval(parse(text = text1))) %>%
    mutate(X1 = GCA_temp) %>%
    select(X1, names(temp1)[1:(length(names(temp1))-1)])

  GCAId <- out_df[["X1"]]

  out_df <- data.frame(t(out_df))
  out_df <- data.frame(out_df[-1 , ])

  colnames(out_df) <- GCAId

  out_df
}
#' get_location
get_location <- function(fourth_data, domain, select_domain, id_list, locNm){

  combined_id <- id_list$GCA
  combined_df <- str_split(combined_id, "-", simplify = T)
  GCA_id <- unique(combined_df[,1])

  if("GCA" %in% domain) {
    if(length(domain) > 1) {
      text1 <- glue("paste({paste(domain, collapse = ',')}, sep = '-')")
      dataUsed <-
        fourth_data %>%
        mutate(GCA_temp0 = eval(parse(text = text1)), .before = GCA) %>%
        filter(GCA %in% GCA_id)

      GCA_GCA <-
        fourth_data %>%
        mutate(GCA_temp0 = paste(GCA, "All", sep = '-'), .before = GCA) %>%
        filter(GCA %in% GCA_id)

      dataUsed <- bind_rows(GCA_GCA, dataUsed) %>% arrange(GCA)
      select_domain <- c("All",select_domain)

    } else {
      dataUsed <-
        fourth_data %>%
        mutate(GCA_temp0 = paste(GCA, "All", sep = '-'), .before = GCA) %>%
        filter(GCA %in% GCA_id)
    }
  } else {
    dataUsed <-
      fourth_data %>%
      mutate(GCA_temp0 = paste(GCA, Domain, sep = '-'), .before = GCA) %>%
      filter(GCA %in% GCA_id)
  }

  if(!is.null(select_domain)) {
    dataUsed <- dataUsed %>%
      mutate(GCA_temp = GCA_temp0) %>%
      separate(GCA_temp0, c("a1","a2"), "-") %>%
      filter(a2 %in% select_domain)
  } else {
    dataUsed <- dataUsed %>%
      mutate(GCA_temp = GCA_temp0)

  }

  GCAId <- combined_id

  location <-
    dataUsed %>%
    mutate(GCA_temp = factor(GCA_temp, levels = GCAId)) %>%
    group_split(GCA_temp) %>%
    map(., ~ .x %>%
          select(GCA_temp, Item_ID, all_of(locNm)) %>%
          rename("GCA" = "GCA_temp"))

  names(location) <- map(location, ~ unique(.x[[1]])) %>% unlist()

  bind_loc <-
    location %>%
    map(., ~ .x %>% arrange(!!as.name(locNm)) %>%
          mutate(OOD = 1:nrow(.)) %>%
          select(-c(1))
    )

  return(list(location = location, bind_loc = bind_loc))
}

