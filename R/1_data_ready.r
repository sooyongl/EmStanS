#'
dataReady_rating <- function(imprt_data) {
  setup <- imprt_data[[1]]
  rating <- imprt_data[[3]]
  itemdata <- imprt_data[[4]]
  itemdata$Item_ID <- as.character(itemdata$Item_ID)
  itemdata$GCA <- factor(itemdata$GCA, levels = imprt_data[[1]][["GCA"]])

  setup_name <- names(setup)
  rating_name <- names(rating)

  rating <-
    left_join(rating,
              itemdata %>% select(Item_ID, starts_with("Order")),
              by = "Item_ID") %>%
    mutate(GCA = factor(GCA, levels = setup$GCA)) %>%
    arrange(GCA, Order_of_Difficulty) %>%
    select(-Order_of_Difficulty)

  ald_p <- get_which(upper_remove_blank(rating_name), "ALD")
  ald_opt_name <- rating_name[ald_p]

  panel_p <- get_which(upper_remove_blank(rating_name), "USE|PANEL")
  lv_p <- get_which(upper_remove_blank(setup_name), "LEVELOPTIONS")
  lv_vec <- map(str_split(setup[[lv_p]], ","), remove_blank_vector)

  new_level_name <-
    rating %>%
    mutate(ALD = remove_blank_vector(ALD)) %>%
    group_split(GCA) %>%
    map(., ~ .x %>% pull(ALD)) %>%
    map2(., lv_vec, ~ paste0("Level", match(.x, .y))) %>%
    unlist(.)

  rating_name[panel_p] <- "Panelist"
  rating[[ald_p]] <- new_level_name
  rating_data <- rating

  return(rating_data)
}
