#' read_data
#'
#' @param filePath a character for a file path.
#' @param sheets_name a list for names of excel sheets
#' @return a named list containing standard setting information (excel data with 5 sheets.)
#' @examples
#' \dontrun{
#' filePath <- "data/freq_data.xlsx"
#' imported_data <- read_data(filePath)
#' }

read_data <- function(filePath, sheets_name) {
  # filePath = path;
  imprt_data_12 <-
    sheets_name[1:2] %>%
    map(read_excel, path = filePath)

  imprt_data_3 <-
    sheets_name[3] %>%
    map(read_excel, path = filePath, col_types = 'text')

  imprt_data_4 <-
    sheets_name[4] %>%
    map(read_excel, path = filePath) %>%
    map(., ~ .x %>% mutate(Item_ID = as.character(Item_ID)))

  imprt_data_5 <-
    sheets_name[5] %>%
    map(read_excel, path = filePath)

  imprt_data <- append(imprt_data_12, imprt_data_3)
  imprt_data <- append(imprt_data, imprt_data_4)
  imprt_data <- append(imprt_data, imprt_data_5)

  imprt_data <-
    imprt_data %>%
    set_names(nm = c("setup","panelist","rating","item_data","examinee_data"))

  return(imprt_data)
}

#' data_ready
#'
#' @param imprt_data a list of imported data using \code{\link{read_data}}
#' @return a list of the data ready for the analysis.
#' @examples
#' \dontrun{
#' require(embededss)
#' filePath <- "data/freq_data.xlsx"
#' imported_data <- read_data(filePath)
#' data_list <- data_ready(imported_data)
#' }

data_ready <- function(imprt_data) { # imprtData = imprt_data

  # IMPORTANT ------------------------------------
  # DATA MUST be in order: setup / panelist / rating / item meta data / examinee data
  # ----------------------------------------------

  setup_data    <- dataReady_setup(imprt_data)
  panelist_data <- dataReady_panelist(imprt_data)
  rating_data   <- dataReady_rating(imprt_data)
  item_data     <- dataReady_itemdata(imprt_data)
  examinee_data <- dataReady_examineedata(imprt_data)

  res <- list(
    setup_data    = setup_data,
    panelist_data = panelist_data,
    rating_data   = rating_data,
    item_data     = item_data,
    examinee_data = examinee_data)

  return(res)
}


#' get_data_info
#'
#' @param data_list a list of prepared data that is generated by \code{\link{data_ready}}
#' @param ... input information for ESS analysis including gca, ald, location, WESS, gamest, domain, select_domain. Those come from the server inputs.
#' @return a list containing all the information necessary for ESS estimation.
#' @examples
#' \dontrun{
#' require(embededss)
#' filePath <- "data/freq_data.xlsx"
#' imported_data <- read_data(filePath)
#' data_list <- data_ready(imported_data)
#' data_information <- get_data_info(data_list, grade = c("M3"), ald = "ALD", location = "Loc_RP60", wess = F, modal = F, threshold = F)
#'
#' }

get_data_info <- function(data_list, ...){

  information <- list()
  inputs <- list(...)
  # inputs <- list(gca = input$tests,
  #                ald = "ALD",
  #                location = input$loc,
  #                WESS = input$WESS,
  #                gamest = input$gamest,
  #                domain  = input$domain,
  #                select_domain = input$select_domain,
  #                modal = F)
  names(inputs) <- tolower(names(inputs))

  setup_data    <- data_list$setup_data
  panelist_data <- data_list$panelist_data
  rating_data   <- data_list$rating_data
  item_data     <- data_list$item_data

  if(sum(data_list$examinee_data == -999) != 0) {
    if("GCA" %in% inputs$domain & length(inputs$domain) == 1) {

      examinee_data <- data_list$examinee_data %>%
        mutate(GCA = paste(GCA, "All", sep = "-"))

      GCA_order <- examinee_data$GCA

    } else if("Domain" %in% inputs$domain & length(inputs$domain) == 1) {

      temp1 <-
        item_data %>%
        select(GCA, Domain) %>%
        arrange(GCA, Domain) %>%
        distinct(GCA, Domain)

      examinee_data <-
        data_list$examinee_data %>%
        left_join(temp1, by = c("GCA")) %>%
        mutate(GCA = paste(GCA, Domain, sep = "-")) %>%
        select(-Domain)

      GCA_order <- examinee_data$GCA

    } else if(length(inputs$domain) == 2) {

      gca_ID <- tibble(GCA = levels(item_data[["GCA"]]), Domain = 'All')
      gca_ID$GCA <- factor(gca_ID$GCA, levels = gca_ID$GCA)

      temp1 <-
        item_data %>%
        select(GCA, Domain) %>%
        arrange(GCA, Domain) %>%
        distinct(GCA, Domain) %>%
        bind_rows(gca_ID, .) %>%
        arrange(GCA)

      examinee_data <-
        data_list$examinee_data %>%
        left_join(temp1, by = c("GCA")) %>%
        mutate(GCA = paste(GCA, Domain, sep = "-")) %>%
        select(-Domain)

      GCA_order <- examinee_data$GCA

    } else {
      examinee_data <- data_list$examinee_data
      GCA_order <- examinee_data$GCA
    }

  } else {

    if("GCA" %in% inputs$domain & length(inputs$domain) == 1) {

      examinee_data <- data_list$examinee_data %>%
        mutate(GCA = paste(GCA, "All", sep = "-"))

      GCA_order <- unique(examinee_data$GCA)

    } else if("Domain" %in% inputs$domain & length(inputs$domain) == 1) {

      temp1 <-
        item_data %>%
        select(GCA, Domain) %>%
        arrange(GCA, Domain) %>%
        distinct(GCA, Domain)

      examinee_data <-
        examinee_data %>%
        left_join(temp1, by = c("GCA")) %>%
        mutate(GCA = paste(GCA, Domain, sep = "-")) %>%
        select(-Domain)

      GCA_order <- unique(examinee_data$GCA)

    } else if(length(inputs$domain) == 2) {

      gca_ID <- tibble(GCA = levels(item_data[["GCA"]]), Domain = 'All')
      gca_ID$GCA <- factor(gca_ID$GCA, levels = gca_ID$GCA)

      temp1 <-
        item_data %>%
        select(GCA, Domain) %>%
        arrange(GCA, Domain) %>%
        distinct(GCA, Domain) %>%
        bind_rows(gca_ID, .) %>%
        arrange(GCA)

      examinee_data <-
        data_list$examinee_data %>%
        left_join(temp1, by = c("GCA")) %>%
        mutate(GCA = paste(GCA, Domain, sep = "-")) %>%
        select(-Domain)

      GCA_order <- unique(examinee_data$GCA)

    } else {
      examinee_data <- data_list$examinee_data
      GCA_order <- unique(examinee_data$GCA)
    }
  }

  data_name <- names(data_list$item_data)
  item_data <- item_data  %>%
    arrange(!!as.name(data_name[1]), !!as.name(inputs$loc))

  if("GCA" %in% inputs$domain) {

    # domain <- ifelse(is.null(inputs$domain), "-", inputs$domain)

    if(length(inputs$domain) > 1) {
      text1 <- glue("paste({paste(inputs$domain, collapse = ',')}, sep = '-')")
      new_GCA <-
        item_data %>%
        select(all_of(inputs$domain), Item_ID) %>%
        filter(Domain %in% inputs[["select_domain"]]) %>%
        mutate(GCA_temp = eval(parse(text = text1))) %>%
        select(GCA_temp, Item_ID)

      GCA_GCA <-
        item_data %>%
        select(GCA, Item_ID) %>%
        mutate(GCA_temp = paste(GCA, "All", sep = '-')) %>%
        select(GCA_temp, Item_ID)

      new_GCA <- bind_rows(GCA_GCA, new_GCA)

    } else {
      new_GCA <-
        item_data %>%
        select(all_of(inputs$domain), Item_ID) %>%
        mutate(GCA_temp = paste(GCA, "All", sep = '-')) %>%
        select(GCA_temp, Item_ID)
    }
  } else {
    new_GCA <-
      item_data %>%
      select(GCA, Domain, Item_ID) %>%
      filter(Domain %in% inputs[["select_domain"]]) %>%
      mutate(GCA_temp = paste(GCA, Domain, sep = '-')) %>%
      select(GCA_temp, Item_ID)
  }

  filtered_data <-
    rating_data %>%
    filter(GCA %in% inputs[["gca"]]) %>%
    left_join(., new_GCA, by = "Item_ID") %>%
    mutate(GCA = GCA_temp) %>%
    select(-GCA_temp) %>%
    mutate(GCA = factor(GCA, levels = GCA_order))

  filtered_data <- filtered_data %>% drop_na()

  id_list <- get_ID(filtered_data)
  SD_data <- get_SD(setup_data, id_list)
  EC_data <- get_EC(setup_data, id_list)

  lv_p <- get_which(remove_blank(names(setup_data)), "LEVELOPTIONS")
  level_nm0 <- setup_data[setup_data$GCA %in% inputs[["gca"]], lv_p]

  information$imported_data <-
    list(
      setup_data     = setup_data,
      panel_data     = panelist_data,
      rating_data    = rating_data,
      item_data      = item_data,
      examinee_data  = examinee_data
    )

  information$base_data <-
    list(
      target_nm     = inputs[["ald"]],
      loc_nm        = inputs[["location"]],
      WESS          = inputs[["wess"]],
      modal         = inputs[["modal"]],
      gamest        = inputs[["gamest"]],
      domain        = inputs[["domain"]],
      select_domain = inputs[["select_domain"]],
      filtered_data = filtered_data
    )

  information$data_ready <-
    list(
      id_list = id_list,
      level_nm = get_lvnm(level_nm0, id_list),
      location_ready = get_location(
        fourth_data = item_data,
        domain = inputs[["domain"]],
        select_domain = inputs[["select_domain"]],
        id_list = id_list,
        locNm = inputs[["location"]]
        ),

      SD_data = SD_data,
      EC_data = EC_data
    )

  .join <- function(x) { # x = a1[[1]]
    GCA_name <- as.character(unique(x$GCA))
    location_info <- information$data_ready$location_ready$bind_loc[[GCA_name]]
    x %>%
      left_join(., location_info[, c("Item_ID","OOD")], by = "Item_ID") %>%
      arrange(OOD) %>%
      select(-OOD)
  }

  information$split_data <-
    filtered_data %>%
    mutate(GCA = factor(GCA, levels = id_list$GCA)) %>%
    group_split(GCA, !!as.name(names(filtered_data)[str_detect(names(filtered_data), "Panel|User")])) %>%
    map(., ~ .join(.x))

  return(information)
}


